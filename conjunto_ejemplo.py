# Crear un conjunto
mi_conjunto = {1, 2, 3, 4, 5}
# Añadir elementos al conjunto
mi_conjunto.add(6)
# Eliminar un elemento del conjunto
mi_conjunto.remove(3)
# Verificar si un elemento está en el conjunto
esta_presente = 4 in mi_conjunto
# Número de elementos en el conjunto
num_elementos = len(mi_conjunto)


'''Los diccionarios son ideales cuando necesitamos almacenar
información en pares clave-valor y acceder a ella a través de claves.

Los conjuntos son útiles cuando necesitamos almacenar elementos
únicos y realizar operaciones matemáticas de conjuntos como uniones,
intersecciones y diferencias.

Los diccionarios y los conjuntos no mantienen un orden específico, por
lo que no se puede asumir el orden de los elementos en ninguna de las
estructuras.
'''
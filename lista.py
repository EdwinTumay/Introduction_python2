# listas
lista = [1,2,3,4]
print(lista)

# Acceso de elementos
lista[2] # esto retorna 3
print(lista[2])

# modificacion de lista
lista[0] = 10 # quedaria [10,2,3,4]
print(lista)

# añadir un nuevo elemento
lista.append(5) # quedaria [10,2,3,4,5]
print(lista)

# Eliminar un elemento
lista.remove(10)
print(lista)

# longitud de una lista
len(lista)
print(len(lista))

